/*1*/
function busy_fun1() {

    if (document.getElementById("busy1").style.display == "none") {
        document.getElementById("busy1").style.display = "block";
        document.getElementById("back1").style.backgroundColor = "#04AA6D";
        document.getElementById("back1").style.color = "white";
    }
    else {
        document.getElementById("busy1").style.display = "none";
        document.getElementById("back1").style.color = "black";
    }
    if (document.getElementById("busy1").style.display == "none") {
        document.getElementById("back1").style.backgroundColor = "rgb(238, 238, 238)";
    }
}
